var dia = parseInt(prompt("Ingrese el día de la semana ( 1 al 7)"));

if (dia == 1) {
    alert("El día de la semana es Lunes");
} else if (dia == 2) {
    alert("El día de la semana es Martes");
}  else if (dia == 3) {
    alert("El día de la semana es Miercoles");
} else if (dia == 4) {
    alert("El día de la semana es Jueves");
} else if (dia == 5) {
    alert("El día de la semana es Viernes");
} else if (dia == 6) {
    alert("El día de la semana es Sábado");
} else if (dia == 7) {
    alert("El día de la semana es Domingo");
} else {
    alert("El número está fuera del rango 1 al 7");
}
